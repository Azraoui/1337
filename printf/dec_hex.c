#include "libftprintf.h"

char	dec_hex(char s, char c, unsigned long nb)
{
	int		n;

	s = nb;
	n = (c == 'X') ? 55 : 87;
	if (s < 10)
		s += 48;
	else
		s += n;
	return (s);
}
