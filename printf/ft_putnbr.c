#include "libftprintf.h"

void	ft_putnbr(int n)
{
	int				i;
	unsigned int	nb;

	if (n < 0)
	{
		ft_putchar('-', 1);
		n = -n;
	}
	nb = n;
	i = 1;
	while ((nb /= 10) > 0)
		i *= 10;
	nb = n;
	while (i)
	{
		ft_putchar((nb / i) + '0', 1);
		nb %= i;
		i /= 10;
	}
}
